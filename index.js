function show() {
    var showElement = document.getElementById("show");
  
    if (showElement.style.display === "block") {
      showElement.style.display = "none";
      let set = document.getElementById("cros")
      set.setAttribute("class", "fa fa-plus")
    } else if (showElement.style.display === "none") {
      showElement.style.display = "block";
      let set = document.getElementById("cros")
      set.setAttribute("class", "fa fa-times")
    } else {
      showElement.style.display = "none";
    }
  }
